package animalFighting;

public class PlayerStats {

	private int hp;
	private int attack;
	private int speed;
	private int defense;
	
	public Player(int hp,int attack, int speed, int defense) {
		this.hp = hp;
		this.attack = attack;
		this.speed = speed;
		this.defense = defense;
	}
	
	public int getHP() {
		return this.hp;
	}
	public void setHP() {
		this.hp;
	}
	
	
	
}
